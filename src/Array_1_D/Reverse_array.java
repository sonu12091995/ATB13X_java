package Array_1_D;

public class Reverse_array {
    public static void main(String[] args) {
      //https://www.programiz.com/online-compiler/6NwNXYwjZBX4R

        int a [] = {1,2,3,4,5};

        for (int i=a.length-1; i>=0; i--)
        {
            System.out.println(a[i]);
        }
    }
}
