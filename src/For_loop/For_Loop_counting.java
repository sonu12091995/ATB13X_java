package For_loop;

public class For_Loop_counting {
    public static void main(String[] args) {

        for (int i = 0; i<10; i++){
            System.out.println(i);// 0 to 9  Run time :10
        }


        for (int _i = 0; _i<=10; _i++){
            System.out.println(_i);// 0 to 10  Run time :11
        }
    }
}
