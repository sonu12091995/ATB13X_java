package String_Methods;

public class toLowerCase_toUpperCase {
    public static void main(String[] args) {
        //Lower case--Converts all characters to lowercase.


        String s = "HELLO";
        String s2 = s.toLowerCase(); // "hello"

//19. toUpperCase()---Converts all characters to uppercase.


        String s3 = "hello";
        String s4 = s.toUpperCase(); // "HELLO"


    }
}
