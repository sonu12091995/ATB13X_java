package Oprators;

public class Decrement_Operator {
    public static void main(String[] args) {
        int a = 10;
        int res = a--;
        System.out.println(a);//9
        System.out.println(res);//10




        int c = 10;
        int res1= --c;
        System.out.println(c);//9
        System.out.println(res1);//9
    }
}
