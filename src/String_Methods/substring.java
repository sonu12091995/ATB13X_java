package String_Methods;

public class substring {
    public static void main(String[] args) {
        //substring(int beginIndex)--Returns a substring from the specified index to the end.


        String s = "Hello";
        String s2 = s.substring(2); // "llo"

// --substring(int beginIndex, int endIndex)--Returns a substring from beginIndex to endIndex-1.


        String s3 = "Hello";
        String s4 = s.substring(1, 3); // "el"


    }
}
